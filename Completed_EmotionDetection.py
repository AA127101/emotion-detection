def wilson_interval(p, n, z=1.96):
    """
    Approximate Wilson score confidence interval.
    Here, p is the hardcoded F1 score and n is class support.
    """
    if n == 0:
        return 0.0, 0.0

    denominator = 1 + (z**2 / n)
    center = (p + (z**2 / (2 * n))) / denominator
    margin = (z / denominator) * np.sqrt(
        (p * (1 - p) / n) + (z**2 / (4 * n**2))
    )

    lower = max(0.0, center - margin)
    upper = min(1.0, center + margin)

    return lower, upper


lower_bounds = []
upper_bounds = []

for f1, n in zip(f1_scores, supports):
    if f1 == 0:
        # Keep zero-F1 classes from showing misleading huge upper error bars
        lower_bounds.append(0.0)
        upper_bounds.append(0.0)
    else:
        low, high = wilson_interval(f1, n)
        lower_bounds.append(low)
        upper_bounds.append(high)

lower_bounds = np.array(lower_bounds)
upper_bounds = np.array(upper_bounds)

lower_errors = f1_scores - lower_bounds
upper_errors = upper_bounds - f1_scores
