# ── Verify loss masking (custom collator; robust template matching) ──────────
print("\n--- Loss Masking Sanity Check ---")

class CompletionOnlyCollator:
    """
    Masks prompt tokens with -100, keeps only completion tokens (label code)
    for loss computation.
    """
    def __init__(self, tokenizer, response_templates, max_length):
        self.tokenizer = tokenizer
        self.max_length = max_length

        # Support multiple template variants for tokenizer edge cases
        if isinstance(response_templates, str):
            response_templates = [response_templates]

        self.response_templates = response_templates
        self.response_ids_list = []
        for t in response_templates:
            ids = tokenizer.encode(t, add_special_tokens=False)
            if len(ids) > 0:
                self.response_ids_list.append((t, ids))

        if len(self.response_ids_list) == 0:
            raise ValueError("All response templates tokenized to empty IDs.")

    @staticmethod
    def _find_subseq(seq, subseq):
        n, m = len(seq), len(subseq)
        for i in range(n - m + 1):
            if seq[i:i+m] == subseq:
                return i
        return -1

    def _to_feature(self, f):
        # Works with pretokenized features or raw rows
        if "input_ids" in f and "attention_mask" in f:
            return {"input_ids": f["input_ids"], "attention_mask": f["attention_mask"]}
        if "text_formatted" in f:
            tok = self.tokenizer(
                f["text_formatted"],
                truncation=True,
                max_length=self.max_length,
                add_special_tokens=True,
            )
            return {"input_ids": tok["input_ids"], "attention_mask": tok["attention_mask"]}
        raise KeyError(f"Feature missing required fields: {f.keys()}")

    def __call__(self, features):
        feats = [self._to_feature(f) for f in features]
        batch = self.tokenizer.pad(feats, return_tensors="pt")

        input_ids = batch["input_ids"]
        attention_mask = batch["attention_mask"]
        labels = input_ids.clone()

        for i in range(input_ids.size(0)):
            valid_len = int(attention_mask[i].sum().item())
            seq = input_ids[i, :valid_len].tolist()

            # Try all template variants and pick first match
            found_start = -1
            found_len = 0
            found_template = None
            for t, ids in self.response_ids_list:
                s = self._find_subseq(seq, ids)
                if s != -1:
                    found_start = s
                    found_len = len(ids)
                    found_template = t
                    break

            if found_start == -1:
                # Safe fallback: mask whole sequence
                labels[i, :valid_len] = -100
            else:
                label_start = found_start + found_len
                labels[i, :label_start] = -100

            # Mask padding
            labels[i, valid_len:] = -100

        batch["labels"] = labels
        return batch

# Use multiple variants because tokenizer behavior can differ by version/model
