"""## Block 15 — Clean Inference Function

Returns human-readable label, class code, confidence, and optional explanation.
Classification accuracy does NOT depend on the explanation step.
"""

print("=== Inference Function ===\n")

EXPLAIN_PROMPT = (
    "You briefly explain emotion classifications. Given text and its classified emotion, "
    "explain in ONE sentence what textual cues support this. Stay grounded in the text. "
    "Do not make medical or diagnostic claims.\n"
    "Text: \"{text}\"\nEmotion: {label}\nExplanation:"
)

def predict(text: str, explain: bool = False, top_k: int = 3) -> dict:
    """Predict emotion via closed-set scoring. Explanation is a separate optional step."""
    name, code_str, scores = closed_set_predict(text)

    # Confidence from scores (softmax over log-probs)
    score_tensor = torch.tensor([scores[i] for i in range(NUM_LABELS)])
    probs = torch.softmax(score_tensor, dim=0)
    confidence = probs[NAME2IDX[name]].item()

    # Top-K
    top_k_indices = probs.argsort(descending=True)[:top_k]
    top_k_list = [{"label": IDX2NAME[i.item()], "code": IDX2CODE[i.item()],
                    "score": round(probs[i].item(), 4)} for i in top_k_indices]

    result = {
        "label": name, "code": code_str, "confidence": round(confidence, 4), "top_k": top_k_list,
    }

    if explain:
        exp_prompt = EXPLAIN_PROMPT.format(text=text[:250], label=name)
        exp_inputs = tokenizer(exp_prompt, return_tensors="pt", truncation=True,
                               max_length=config.max_seq_length).to(model.device)
        with torch.no_grad():
            exp_out = model.generate(**exp_inputs, max_new_tokens=80,
                                     do_sample=True, temperature=0.3, top_p=0.9)
        explanation = tokenizer.decode(
            exp_out[0][exp_inputs.input_ids.shape[1]:], skip_special_tokens=True
        ).strip().split(".")[0] + "."
        result["explanation"] = explanation

    return result

