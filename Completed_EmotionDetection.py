"""## Block 12 — Closed-Set Scoring Function

Instead of free-form generation, we compute the log-probability of each of the
28 label codes given the prompt prefix. The code with the highest probability wins.
This eliminates invalid outputs entirely and is the primary evaluation method.
"""

print("=== Closed-Set Scoring Setup ===\n")

FastLanguageModel.for_inference(model)

# Pre-tokenize all 28 codes
CODE_TOKEN_IDS = {}
for idx in range(NUM_LABELS):
    code_str = IDX2CODE[idx]
    ids = tokenizer.encode(code_str, add_special_tokens=False)
    CODE_TOKEN_IDS[idx] = ids

print("Code tokenizations:")
for idx in range(NUM_LABELS):
    ids = CODE_TOKEN_IDS[idx]
    decoded = tokenizer.decode(ids)
    print(f"  {IDX2CODE[idx]} ({IDX2NAME[idx]:20s}) -> {ids} -> '{decoded}'")

def closed_set_predict(text: str) -> tuple:
    """Score all 28 label codes, return (predicted_name, predicted_code, scores_dict)."""
    prompt = PROMPT_TEMPLATE.format(text=text)
    prompt_ids = tokenizer.encode(prompt, add_special_tokens=True)
    prompt_len = len(prompt_ids)

    best_idx = -1
    best_score = float("-inf")
    all_scores = {}

    for idx in range(NUM_LABELS):
        code_ids = CODE_TOKEN_IDS[idx]
        full_ids = prompt_ids + code_ids
        input_tensor = torch.tensor([full_ids], device=model.device)

        with torch.no_grad():
            outputs = model(input_tensor)

        logits = outputs.logits[0]
        log_probs = torch.log_softmax(logits, dim=-1)

        total_lp = 0.0
        for i, tid in enumerate(code_ids):
            pos = prompt_len - 1 + i
            total_lp += log_probs[pos, tid].item()

        all_scores[idx] = total_lp
        if total_lp > best_score:
            best_score = total_lp
            best_idx = idx

    return IDX2NAME[best_idx], IDX2CODE[best_idx], all_scores

